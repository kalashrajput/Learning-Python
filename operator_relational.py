#Using Relational Operators :
a=int(input("Enter a number : "));
b=int(input("Enter another number : "));
#Greater than (>)
print(a>b);
#Greater than equal to (>=)
print(a>=b);
#Less than (<)
print(a<b);
#Less than equal to (<=)
print(a<=b);
#Equal to (==)
print(a==b);
#Not equal to (!=)
print(a!=b);
