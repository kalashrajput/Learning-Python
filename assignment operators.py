#Using Assignment Operators :
a=int(input("Enter value of a : "));
b=int(input("Enter value of b : "));
c=int(input("Enter value of c : "));
#Assign(=)
c=a+b;
print(c);
#Add and assign (+=)
c+=a;
print(c);
#Subtract and assign (-=)
c-=a;
print(c);
#Multiply and assign (*=)
c*=a;
print(c);
#Divide and assign (/=)
c/=a;
print(c);
#Modulus and assign (%=)
c%=a;
print(c);
